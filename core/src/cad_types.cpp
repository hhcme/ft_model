Matrix4x4 Matrix4x4::inverse() const {
    // Full 4x4 matrix inverse using cofactor expansion
    const float* a = &m[0][0];

    float inv[16];
    inv[0]  =  a[5]*a[10]*a[15] - a[5]*a[11]*a[14] - a[9]*a[6]*a[15] + a[9]*a[7]*a[14] + a[13]*a[6]*a[11] - a[13]*a[7]*a[10];
    inv[4]  = -a[4]*a[10]*a[15] + a[4]*a[11]*a[14] + a[8]*a[6]*a[15] - a[8]*a[7]*a[14] - a[12]*a[6]*a[11] + a[12]*a[7]*a[10];
    inv[8]  =  a[4]*a[9]*a[15]  - a[4]*a[11]*a[13] - a[8]*a[5]*a[15] + a[8]*a[7]*a[13] + a[12]*a[5]*a[11] - a[12]*a[7]*a[9];
    inv[12] = -a[4]*a[9]*a[14]  + a[4]*a[10]*a[13] + a[8]*a[5]*a[14] - a[8]*a[6]*a[13] - a[12]*a[5]*a[10] + a[12]*a[6]*a[9];
    inv[1]  = -a[1]*a[10]*a[15] + a[1]*a[11]*a[14] + a[9]*a[2]*a[15] - a[9]*a[3]*a[14] - a[13]*a[2]*a[11] + a[13]*a[3]*a[10];
    inv[5]  =  a[0]*a[10]*a[15] - a[0]*a[11]*a[14] - a[8]*a[2]*a[15] + a[8]*a[3]*a[14] + a[12]*a[2]*a[11] - a[12]*a[3]*a[10];
    inv[9]  = -a[0]*a[9]*a[15]  + a[0]*a[11]*a[13] + a[8]*a[1]*a[15] - a[8]*a[3]*a[13] - a[12]*a[1]*a[11] + a[12]*a[3]*a[9];
    inv[13] =  a[0]*a[9]*a[14]  - a[0]*a[10]*a[13] - a[8]*a[1]*a[14] + a[8]*a[2]*a[13] + a[12]*a[1]*a[10] - a[12]*a[2]*a[9];
    inv[2]  =  a[1]*a[6]*a[15]  - a[1]*a[7]*a[14]  - a[5]*a[2]*a[15] + a[5]*a[3]*a[14] + a[13]*a[2]*a[7]  - a[13]*a[3]*a[6];
    inv[6]  = -a[0]*a[6]*a[15]  + a[0]*a[7]*a[14]  + a[4]*a[2]*a[15] - a[4]*a[3]*a[14] - a[12]*a[2]*a[7]  + a[12]*a[3]*a[6];
    inv[10] =  a[0]*a[5]*a[15]  - a[0]*a[7]*a[13]  - a[4]*a[1]*a[15] + a[4]*a[3]*a[13] + a[12]*a[1]*a[7]  - a[12]*a[3]*a[5];
    inv[14] = -a[0]*a[5]*a[14]  + a[0]*a[6]*a[13]  + a[4]*a[1]*a[14] - a[4]*a[2]*a[13] - a[12]*a[1]*a[6]  + a[12]*a[2]*a[5];
    inv[3]  = -a[1]*a[6]*a[11]  + a[1]*a[7]*a[10]  + a[5]*a[2]*a[11] - a[5]*a[3]*a[10] - a[9]*a[2]*a[7]   + a[9]*a[3]*a[6];
    inv[7]  =  a[0]*a[6]*a[11]  - a[0]*a[7]*a[10]  - a[4]*a[2]*a[11] + a[4]*a[3]*a[10] + a[8]*a[2]*a[7]   - a[8]*a[3]*a[6];
    inv[11] = -a[0]*a[5]*a[11]  + a[0]*a[7]*a[9]   + a[4]*a[1]*a[11] - a[4]*a[3]*a[9]  - a[8]*a[1]*a[7]   + a[8]*a[3]*a[5];
    inv[15] =  a[0]*a[5]*a[10]  - a[0]*a[6]*a[9]   - a[4]*a[1]*a[10] + a[4]*a[2]*a[9]  + a[8]*a[1]*a[6]   - a[8]*a[2]*a[5];

    float det = a[0]*inv[0] + a[1]*inv[4] + a[2]*inv[8] + a[3]*inv[12];
    if (std::abs(det) < 1e-10f) {
        return identity(); // Singular matrix, return identity
    }

    float inv_det = 1.0f / det;
    Matrix4x4 result;
    for (int i = 0; i < 16; ++i) {
        (&result.m[0][0])[i] = inv[i] * inv_det;
    }
    return result;
}

Matrix4x4 Matrix4x4::inverse_2d() const {
    // Optimized inverse for 2D affine transform:
    // [m00 m01  0  m03]
    // [m10 m11  0  m13]
    // [  0   0  1    0]
    // [  0   0  0    1]
    // Inverse of 2x2 rotation/scale part + negated translation
    float det = m[0][0] * m[1][1] - m[0][1] * m[1][0];
    if (std::abs(det) < 1e-10f) {
        return identity();
    }
    float inv_det = 1.0f / det;
    Matrix4x4 result;
    result.m[0][0] =  m[1][1] * inv_det;
    result.m[0][1] = -m[0][1] * inv_det;
    result.m[1][0] = -m[1][0] * inv_det;
    result.m[1][1] =  m[0][0] * inv_det;
    result.m[3][0] = -(result.m[0][0] * m[3][0] + result.m[1][0] * m[3][1]);
    result.m[3][1] = -(result.m[0][1] * m[3][0] + result.m[1][1] * m[3][1]);
    result.m[2][2] = 1.0f;
    result.m[3][3] = 1.0f;
    return result;
}

Matrix4x4 Matrix4x4::orthographic(float left, float right, float bottom, float top) {
    Matrix4x4 mat;
    float rl = right - left;
    float tb = top - bottom;
    if (std::abs(rl) < 1e-10f || std::abs(tb) < 1e-10f) return identity();

    mat.m[0][0] = 2.0f / rl;
    mat.m[1][1] = 2.0f / tb;
    mat.m[2][2] = 1.0f; // 2D: no depth scaling
    mat.m[3][0] = -(right + left) / rl;
    mat.m[3][1] = -(top + bottom) / tb;
    mat.m[3][3] = 1.0f;
    return mat;
}

